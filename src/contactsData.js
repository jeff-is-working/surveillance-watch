// Helper function to search contacts
export function searchContacts(searchTerm) {
  if (!searchTerm) return CONTACTS_DATA;
  const term = searchTerm.toLowerCase();
  return CONTACTS_DATA.filter(agency => 
    agency.City.toLowerCase().includes(term) ||
    agency.County.toLowerCase().includes(term) ||
    agency.Email.toLowerCase().includes(term)
  );
}

// Helper function to get contacts by county
export function getContactsByCounty(county) {
  return CONTACTS_DATA.filter(agency => 
    agency.County.toLowerCase() === county.toLowerCase()
  );
}

// Helper function to get all counties
export function getAllCounties() {
  const counties = [...new Set(CONTACTS_DATA.map(agency => agency.County))];
  return counties.sort();
}

// Helper function to get all cities
export function getAllCities() {
  const cities = [...new Set(CONTACTS_DATA.map(agency => agency.City))];
  return cities.sort();
}
